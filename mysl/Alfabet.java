public class Alfabet {
	public static void main(String[] args) {
		System.out.print("Alfabet ³aciñski ");
		for(char c = 'A';c<='Z';c++) {
			System.out.print(c);
		}
		System.out.println();
		System.out.println();
		System.out.print("Alfabet grecki ");
		for(int i = 913;i<=937;i++) {
			System.out.print((char) i);
		}
		System.out.println();
	}
}